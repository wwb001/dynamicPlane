package cn.wwb.vo;

public class CKLS {
	private String FLID, FFID, FATT, FCES, FCEE, CODE, CKAT, BTSC;

	public String getFLID() {
		return FLID;
	}

	public void setFLID(String fLID) {
		FLID = fLID;
	}

	public String getFFID() {
		return FFID;
	}

	public void setFFID(String fFID) {
		FFID = fFID;
	}

	public String getFATT() {
		return FATT;
	}

	public void setFATT(String fATT) {
		FATT = fATT;
	}

	public String getFCES() {
		return FCES;
	}

	public void setFCES(String fCES) {
		FCES = fCES;
	}

	public String getFCEE() {
		return FCEE;
	}

	public void setFCEE(String fCEE) {
		FCEE = fCEE;
	}

	public String getCODE() {
		return CODE;
	}

	public void setCODE(String cODE) {
		CODE = cODE;
	}

	public String getCKAT() {
		return CKAT;
	}

	public void setCKAT(String cKAT) {
		CKAT = cKAT;
	}

	public String getBTSC() {
		return BTSC;
	}

	public void setBTSC(String bTSC) {
		BTSC = bTSC;
	}
	
}
