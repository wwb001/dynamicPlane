package cn.wwb.vo;

public class ARRE {
	private String FLID, FATT, FFID, ISTA, FRLT;

	public String getFLID() {
		return FLID;
	}

	public void setFLID(String fLID) {
		FLID = fLID;
	}

	public String getFATT() {
		return FATT;
	}

	public void setFATT(String fATT) {
		FATT = fATT;
	}

	public String getFFID() {
		return FFID;
	}

	public void setFFID(String fFID) {
		FFID = fFID;
	}

	public String getISTA() {
		return ISTA;
	}

	public void setISTA(String iSTA) {
		ISTA = iSTA;
	}

	public String getFRLT() {
		return FRLT;
	}

	public void setFRLT(String fRLT) {
		FRLT = fRLT;
	}
	
	
}
