package cn.wwb.vo;

public class CANE {
	private String FLID, FFID, FATT, ABST, IAST, ABRS, IARS;

	public String getFLID() {
		return FLID;
	}

	public void setFLID(String fLID) {
		FLID = fLID;
	}

	public String getFFID() {
		return FFID;
	}

	public void setFFID(String fFID) {
		FFID = fFID;
	}

	public String getFATT() {
		return FATT;
	}

	public void setFATT(String fATT) {
		FATT = fATT;
	}

	public String getABST() {
		return ABST;
	}

	public void setABST(String aBST) {
		ABST = aBST;
	}

	public String getIAST() {
		return IAST;
	}

	public void setIAST(String iAST) {
		IAST = iAST;
	}

	public String getABRS() {
		return ABRS;
	}

	public void setABRS(String aBRS) {
		ABRS = aBRS;
	}

	public String getIARS() {
		return IARS;
	}

	public void setIARS(String iARS) {
		IARS = iARS;
	}
	

}
