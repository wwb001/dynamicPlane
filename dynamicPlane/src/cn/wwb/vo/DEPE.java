package cn.wwb.vo;

public class DEPE {
	private String FLID, FFID, FATT, STAT, ISTA, FRTT;

	public String getFLID() {
		return FLID;
	}

	public void setFLID(String fLID) {
		FLID = fLID;
	}

	public String getFFID() {
		return FFID;
	}

	public void setFFID(String fFID) {
		FFID = fFID;
	}

	public String getFATT() {
		return FATT;
	}

	public void setFATT(String fATT) {
		FATT = fATT;
	}

	public String getSTAT() {
		return STAT;
	}

	public void setSTAT(String sTAT) {
		STAT = sTAT;
	}

	public String getISTA() {
		return ISTA;
	}

	public void setISTA(String iSTA) {
		ISTA = iSTA;
	}

	public String getFRTT() {
		return FRTT;
	}

	public void setFRTT(String fRTT) {
		FRTT = fRTT;
	}
	
}
